package Day7cw;

public class SquareOrCube {

	public static int Square() {
		int num=30;
		int square=num*num;
		return square;
	}	
		public static int Cube() {
		int num=40;
		int cube=num*num*num;
		return cube;
	}
	public static void main(String[] args) {
		System.out.println("Square ="+Square());
		System.out.println("Cube ="+Cube());
	}
	
}
	
	
		
	

